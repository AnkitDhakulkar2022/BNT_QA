package Ankit_23;



public class EvenOrOdd {

	
		public void evenodd(int i)
		{
		   if(i%2==0)
		   {
			   System.out.println("Given number is even");
		   }else{
			   System.out.println("Given number is odd");
		   }
		}
	 

		public static void main(String[] args) {
			EvenOrOdd n= new EvenOrOdd();
			n.evenodd(6);
		}

	}


